class MenuBorders:

    @property 
    def horizontal_border(self): return '\u2500'

    @property
    def vertical_border(self): return '\u2502'

    @property
    def top_left_corner(self): return '\u250C'

    @property
    def top_right_corner(self): return '\u2510'

    @property
    def bottom_left_corner(self): return '\u2514'

    @property
    def bottom_right_corner(self): return '\u2518'